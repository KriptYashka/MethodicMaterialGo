package main

import "fmt"

func isPrime(n int) bool {
	if n < 2 {
		return false
	}
	for i := 2; i*i <= n; i++ {
		if n%i == 0 {
			return false
		}
	}
	return true
}

func main() {
	var n int
	fmt.Scan(&n)

	nums := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Scan(&nums[i])
	}

	for _, v := range nums {
		if isPrime(v) {
			fmt.Printf("%d: yes\n", v)
		} else {
			fmt.Printf("%d: no\n", v)
		}
	}
}
